export const getConstituentById = (id: string) =>
  constituents.find((c) => c.id === id);

export const getGiftsByConstituent = (constituentId: string) =>
  gifts.filter((g) => g.constituentId === constituentId);

export const getGiftsByFund = (fundId: string) =>
  gifts.filter((g) => g.fund === fundId);

export const getGiftsByCampaign = (campaignId: string) =>
  gifts.filter((g) => g.campaign === campaignId);

export const getFundById = (id: string) =>
  funds.find((f) => f.id === id);

export const getCampaignById = (id: string) =>
  campaigns.find((c) => c.id === id);

export const getAppealsByCampaign = (campaignId: string) =>
  appeals.filter((a) => a.campaignId === campaignId);
